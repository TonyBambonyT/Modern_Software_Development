# Одномерный массив из 10-и целых чисел заполнить с клавиатуры,
# определить сумму тех чисел, которые > 5.

array_2 = []
print("Введите 10 целых чисел для второго задания:")

for i in range(10):
    num = int(input())
    array_2.append(num)

sum_greater_5 = 0

for x in array_2:
    if x > 5:
        sum_greater_5 += x

print("Сумма чисел, которые больше 5:", sum_greater_5)