# Задача: Проверка числа на кратность 7.

# Запрашиваем число и сразу приводим к int
number = int(input("Введите целое число для проверки: "))

# Проверяем кратность 7 через % - остаток 0 означает делится нацело
if number % 7 == 0:
    print("Число", number, "кратно 7")
else:
    print("Число", number, "не кратно 7")